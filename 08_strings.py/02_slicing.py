# In this you have to break the string inn different slices or parts
name = "Sayyam"
print(name[0:3])
# In this it will print the starting 3 letter because index start from 0 to n-1 

# Now if we use negative slicing 
print(name[-3:-1])

# Now we are doing with some steps 
print(name[0:6:2]) 

# Reverse the string 
print(name[::-1])
